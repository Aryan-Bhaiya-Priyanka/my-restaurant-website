# 1. गिटहब से आपका यूजरनेम ऑटोमैटिक निकाल रहे हैं
USER_NAME=$(gh api user -q ".login")

# 2. सारा कोड गिटहब पर मास्टर (master) ब्रांच में पक्का कर रहे हैं
git add .
git commit -m "वेबसाइट को लाइव करने के लिए फाइनल अपडेट"
git push origin master

# 3. टर्मक्स से ही GitHub API के जरिए वेबसाइट को सीधा लाइव कर रहे हैं
echo "गिटहब पर आपकी वेबसाइट होस्ट की जा रही है..."
gh api --method POST /repos/$USER_NAME/my-restaurant-website/pages \
  -f "source[branch]=master" \
  -f "source[path]=/" > /dev/null 2>&1 || true

# 4. काम खत्म! अब आपको आपका डायरेक्ट लिंक दे रहे हैं
clear
echo "================================================================="
echo "🎉 आर्यन भैया, आपके रेस्टोरेंट की वेबसाइट एकदम लाइव हो गई है!"
echo "👉 लिंक: https://$USER_NAME.github.io/my-restaurant-website/"
echo "================================================================="
echo "नोट: गिटहब सर्वर को इसे चालू करने में 1-2 मिनट लग सकते हैं।"
echo "बस थोड़ा सा वेट करके इस लिंक को अपने क्रोम ब्राउज़र में खोल लो।"
